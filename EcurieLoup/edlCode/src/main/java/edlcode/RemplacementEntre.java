package edlcode;

public class RemplacementEntre {
	private String aRemplacer;
	private String remplacement;
	
	
	
	public RemplacementEntre(String aRemplacer, String remplacement) {
		this.aRemplacer = aRemplacer;
		this.remplacement = remplacement;
	}
	public String getARemplacer() {
		return aRemplacer;
	}
	public String getRemplacement() {
		return remplacement;
	}
}
